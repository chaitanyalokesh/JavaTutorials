package day5;

public class DoWhileLoop {

	public static void main(String[] args) {

//Print 1 to 10 using do while loop
		
		int i=1;
		
		do
		{
			System.out.println(i);
			i++;
		}while(i<=10);
		
		System.out.println("------------");
		
//print 10 to 1
		
		int a=10;
		do
		{
			System.out.println(a);
			a--;
		}while(a>0);
	}

}
