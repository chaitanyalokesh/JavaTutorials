package Assignments;

public class day4_PrintMonthNo {

	public static void main(String[] args) {

		String monthname="december";
		
		switch(monthname)
		{
		case "january":System.out.println("Month No. is 1"); break;
		case "february":System.out.println("Month No. is 2"); break;
		case "march":System.out.println("Month No. is 3"); break;
		case "april":System.out.println("Month No. is 4"); break;
		case "may":System.out.println("Month No. is 5"); break;
		case "june":System.out.println("Month No. is 6"); break;
		case "july":System.out.println("Month No. is 7"); break;
		case "august":System.out.println("Month No. is 8"); break;
		case "september":System.out.println("Month No. is 9"); break;
		case "october":System.out.println("Month No. is 10"); break;
		case "november":System.out.println("Month No. is 11"); break;
		case "december":System.out.println("Month No. is 12"); break;
		default:System.out.println("Invalid month");





		
		}
	
	}

}
