package day2;

public class DayTypesDemo2 {

	public static void main(String[] args) {
int a=100,b=200;
System.out.println("The value of a is: "+a);
System.out.println("The value of a is: "+b);


long l= 124475745343L;     //L back end float
System.out.println(l);


byte person_age = 30;
System.out.println(person_age);


float intem_price = 150.25F;   //F back end for float
System.out.println(intem_price);


char  stu_grad = 'A';			// for char in single ''
System.out.println(stu_grad);   //A


boolean status=false;
System.out.println(status);   //Boolean allowed only true/false;


String stu_name="krishna";		//string in double " "
System.out.println(stu_name);





	}

}
